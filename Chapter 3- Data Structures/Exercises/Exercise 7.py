print ("Sorted:")
dream_list1 = ['JAPAN', 'SOUTH KOREA', 'SWITZERLAND', 'FRANCE', 'SPAIN']
x = sorted(dream_list1)
print (x)
print()

print ("Sorted & Reversed:")
dream_list2 = ['JAPAN', 'SOUTH KOREA', 'SWITZERLAND', 'FRANCE', 'SPAIN']
x = sorted(dream_list2, reverse=True)
print (x)
print()

print ("Reversed:")
dream_list3 = ['JAPAN', 'SOUTH KOREA', 'SWITZERLAND', 'FRANCE', 'SPAIN']
dream_list3.reverse()
print (dream_list3)
print()

print ("Reversed back:")
dream_list4 = ['SPAIN', 'FRANCE', 'SWITZERLAND', 'SOUTH KOREA', 'JAPAN']
dream_list4.reverse()
print (dream_list4)
print()

print ("Sort:")
